#include "Driver.h"

bool Driver::install_driver() {
    wchar_t cmd[2048] = { 0 };

    cleanup();

    if (!create_driver_file()) return false;

    wcscat_s(cmd, L"sc.exe create RTCore64 type=kernel start=auto binPath=\"");
    wcscat_s(cmd, driver_path.wstring().c_str());
    wcscat_s(cmd, L"\" DisplayName=\"Micro - Star MSI Afterburner\"");

    wprintf(L"%ws\n", cmd);

    wprintf(L"%d\n", _wsystem(cmd));
    wprintf(L"%d\n", _wsystem(L"net start RTCore64"));

    wprintf(L"Successfully installed driver!\n");
    return true;
}

void Driver::delete_driver_file() {
    HANDLE hDriverFile = CreateFileW(driver_path.wstring().c_str(), DELETE, NULL, NULL, OPEN_EXISTING, FILE_FLAG_OPEN_REPARSE_POINT | FILE_FLAG_DELETE_ON_CLOSE, NULL);

    if (hDriverFile != INVALID_HANDLE_VALUE) {
        CloseHandle(hDriverFile);
    }
}

bool Driver::create_driver_file() {
    HANDLE hDriverFile = CreateFileW(driver_path.wstring().c_str(), GENERIC_READ | GENERIC_WRITE, NULL, NULL, CREATE_NEW, FILE_ATTRIBUTE_NORMAL | FILE_FLAG_OPEN_REPARSE_POINT, NULL);
    if (hDriverFile == INVALID_HANDLE_VALUE) {
        wprintf(L"Failed to create file for driver, Error Code: %d Retrying...\n", GetLastError());
        delete_driver_file();
        hDriverFile = CreateFileW(driver_path.wstring().c_str(), GENERIC_READ | GENERIC_WRITE, NULL, NULL, CREATE_NEW, FILE_ATTRIBUTE_NORMAL | FILE_FLAG_OPEN_REPARSE_POINT, NULL);
        if (hDriverFile == INVALID_HANDLE_VALUE) {
            wprintf(L"Failed to create file for driver, Error Code: %d \n", GetLastError());
            return false;
        }
    }

    if (!WriteFile(hDriverFile, RTCore64, RTCore64_Size, NULL, NULL)) {
        wprintf(L"Unable to write to driver file, %d\n", GetLastError());
        return false;
    }

    CloseHandle(hDriverFile);
    wprintf(L"Created driver file!\n");
    return true;
}

void Driver::uninstall_driver() {
    return Driver::cleanup();
}

void Driver::cleanup() {
    wprintf(L"%d\n", _wsystem(L"net stop RTCore64"));
    wprintf(L"%d\n", _wsystem(L"sc.exe delete RTCore64"));
    Sleep(5000); 
    delete_driver_file();
}

